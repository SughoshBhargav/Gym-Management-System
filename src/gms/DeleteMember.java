    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed

        ResultSet rs;
        PreparedStatement ps;
        
       search_clicked = true;
       String memid = member_id.getText();
       String sql = "select * from member where memberid = '"+memid+"'";
       
       PLANFIELD.setEditable(false);
       GENDERFIELD.setEditable(false);

        try { 
            ps=MyConnection.getConnection().prepareStatement(sql);
            rs = ps.executeQuery(sql);
       
            if(rs.next())
            {
                NAMEFIELD.setText(rs.getString(1));
                CONTACTFIELD.setText(rs.getString(2));
                ADDRESSFIELD.setText(rs.getString(4));
                AMOUNTFIELD.setText(rs.getString(7 ));
                GENDERFIELD.setSelectedItem(rs.getString(5));
                PLANFIELD.setSelectedItem(rs.getString(6));
                EMAILFIELD.setText(rs.getString(3));
                confirmation = true;
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Member Not Found","Error",JOptionPane.ERROR_MESSAGE);
            }

        } 
         catch (SQLException ex) {
            ex.printStackTrace();
        }
           
    }//GEN-LAST:event_searchActionPerformed

