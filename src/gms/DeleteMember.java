    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        
        
        if(confirmation){
            
            String search_res = member_id.getText();
            int a=JOptionPane.showConfirmDialog(null,"Do you really want to Delete ?","select",JOptionPane.YES_NO_OPTION);
            
            if(a==1)
            {
                this.dispose();
            }
            else
            {
                String sql = "Delete from member where memberid =?";
                try {
                    
                    PreparedStatement ps = MyConnection.getConnection().prepareStatement(sql);
                    ps.setString(1, search_res);
                    ps.execute(); 
                    
                    JOptionPane.showMessageDialog(null, "Member " + search_res + " Deleted");
                } 
                catch (SQLException e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(null, "Member " + search_res + " not found","Error",JOptionPane.ERROR_MESSAGE);
                }
            }
            this.dispose();
            new AdminSite().setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(rootPane,"Search a Member","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

